package modele;

import genreiquedao.ObjetBdd;
import java.util.Vector;

/**
 *
 * @author Nomena
 */
public class Genre extends ObjetBdd{
    int id;
    String nom;
    
    public Vector<Genre> getAll()throws Exception{
        return this.find("select*from genre");
    }
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
}
